public class Student {

    private String sId;
    private String sName;

    //Constructor
    public Student(String studentId, String studentName) {
        this.sId = studentId;
        this.sName = studentName;
    }

    //Getter
    public String getsId() {
        return sId;
    }
    public String getsName() {
        return sName;
    }

}
